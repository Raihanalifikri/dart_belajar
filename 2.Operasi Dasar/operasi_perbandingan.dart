void main(){

  var a = 5;
  var b = 10;

 // Tanda > lebih besar
 print(a > b);

 //Tanda lebih kecil
 print(a < b);

 // Tanda > Lebih besar / sama dengan > =
 print(a >= b);

//  Tanda < lebih kecil / sma dengan < =
 print(a <= b);

// Tanda  = sama dengan sama dengan ==
 print(a == b);

// Tanda != tidak sama dengan
 print(a != b);

// tanda untuk menegaskan
 print(!(a > b));

// Tnda perbandingan &&
 print((b > a) && (a < b));

// tanda || atau
print((a > b) || (b < a));
}