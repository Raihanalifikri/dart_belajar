void main(List<String> args) {
// break
  for (int i = 0; i <= 10; i++) {
    if (i == 5) {
      break; // jika i = 5 maka perulangan akan berhenti fungsi break
    }
    print(i);
  }

  //continue
  for(int i = 0; i <= 10; i++){
    if (i == 5) {
      continue; // jika i = 5 maka perulangan akan di lewati fungsi continue 
    }
    print(i);
  }

  
}

